const _Game_Screen_clear = Game_Screen.prototype.clear;
Game_Screen.prototype.clear = function() {
	_Game_Screen_clear.apply(this, arguments);
	this._playerRegionId = -1;
	this._playerDefaultZoom = true;
};

const _Game_Screen_updateZoom = Game_Screen.prototype.updateZoom;
Game_Screen.prototype.updateZoom = function() {
	_Game_Screen_updateZoom.apply(this, arguments);
	this.updateRegionZoom();
};

Game_Screen.prototype.updateRegionZoom = function(force) {
	if((this._playerRegionId !== $gamePlayer.regionId() || !!force) && $dataMap) {
		this._playerRegionId = $gamePlayer.regionId();
		if(_.zooms[this._playerRegionId] || !this._playerDefaultZoom) {
			let info = null;
			if(_.zooms[this._playerRegionId] && this._playerRegionId !== 0) {
				info = _.zooms[this._playerRegionId];
				this._playerDefaultZoom = false;
			} else {
				info = {
					focus: "player",
					scale: SRD.CameraCore.zoom,
					duration: _.defaultDuration
				};
				this._playerDefaultZoom = true;
			}
			this.setupRegionZoom(info);
		}
	}
};

Game_Screen.prototype.setupRegionZoom = function(info) {
	const focus = info.focus;
	const scale = info.scale;
	const dur = eval(info.duration);
	this.setCameraFocus(String(info.scale), String(info.duration));
	if(focus.match(/event\s*(\d+)/)) {
		const eventId = parseInt(RegExp.$1);
		if($gameMap.event(eventId)) {
			this.focusEvent = 9;
			$gameMap.event(eventId).centerCamera(60);
		}
	} else if(focus.match(/follower\s*(\d+)/)) {
		const followerId = parseInt(RegExp.$1);
		if($gamePlayer.followers().follower(followerId - 1)) {
			this.focusEvent = (followerId * (-1));
			$gamePlayer.followers().follower(followerId - 1).centerCamera(dur);
		}
	} else if(focus === 'player') {
		this.focusEvent = 0;
		$gamePlayer.centerCamera(dur);
	} else if(focus.match(/shift\s*(.+)\s*,\s*(.+)/i)) {
		const coords = focus.match(/shift\s*(.+)\s*,\s*(.+)/i);
		$gameMap.shiftCameraPosition(eval(coords[1]), eval(coords[2]), dur);
	} else {
		const coords = focus.split(/\s*,\s*/);
		this.focusEvent = null;
		$gamePlayer.centerCameraOnPos(eval(coords[0]), eval(coords[1]), dur);
	}
};

