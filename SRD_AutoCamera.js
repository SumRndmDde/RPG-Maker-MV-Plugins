_.meetsRequirements = Imported["SumRndmDde Camera Core"];
_.defaultDuration = String(params['Reset Duration']);

_.zooms = [];
for(let i = 1; i <= 100; i++) {
	let id = String(params['Region ' + i + ' ID']);
	if(id.trim().length > 0) {
		id = parseInt(id);
		_.zooms[id] = {};
		_.zooms[id].focus = String(params['Region ' + i + ' Focus']);
		_.zooms[id].scale = parseFloat(params['Region ' + i + ' Zoom']);
		_.zooms[id].duration = String(params['Region ' + i + ' Duration']);
	}
}

_.alertNeedCameraCore = function() {
	alert("The 'SRD_CameraCore' plugin is required for using the 'SRD_AutoCamera' plugin.");
	if(confirm("Do you want to open the download page to 'SRD_CameraCore'?")) {
		window.open('http://sumrndm.site/camera-core/');
	}
};

if(!_.meetsRequirements) {
	_.alertNeedCameraCore();
}

